use rust_vc_utils::rev_comp;

pub struct AnnotationSeq {
    pub label: Vec<u8>,

    pub seq: Vec<u8>,

    /// rev comp is pre-computed
    pub rev_comp_seq: Vec<u8>,
}

impl AnnotationSeq {
    pub fn new(label: &[u8], seq: &[u8]) -> Self {
        let label = label.to_vec();
        let rev_comp_seq = rev_comp(seq);
        let seq = seq.to_vec();

        Self {
            label,
            seq,
            rev_comp_seq,
        }
    }
}

